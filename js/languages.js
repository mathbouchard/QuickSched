function tr(val)
{
    if($.qsglobal.lang[val] == null || $.qsglobal.lang[val] == undefined || $.qsglobal.lang[val][$.qsglobal.currlang] == "")
        return val;
    else
        return $.qsglobal.lang[val][$.qsglobal.currlang]; 
}

